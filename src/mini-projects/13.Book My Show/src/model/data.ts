//Utils function to generate the Data

function generateSeatData(numCategories: number, rowsPerCategory:number, seatsPerRow: number) {
  const categories = [];

  for (let i = 0; i < numCategories; i++) {
    const category = {
      categoryName: `Category ${i + 1}`,
      price: Math.floor(Math.random() * 1000) + 300,
      seatRows: [],
    };

    for (let j = 0; j < rowsPerCategory; j++) {
      const row = {
        rowNumber: String.fromCharCode(65 + j), // ASCII for row letters starting from 'A'
        seats: [],
      };

      for (let k = 0; k < seatsPerRow; k++) {
        const seat = {
          seatNumber: k + 1,
          id: Math.random().toString(36).substring(7), // Random seat ID
          status: getRandomStatus(),
        };

        row.seats.push(seat);
      }

      category.seatRows.push(row);
    }

    categories.push(category);
  }

  return categories;
}

function getRandomStatus() {
  const statuses = ["AVAILABLE", "BOOKED", "BLOCKED", "NO_SEAT"];
  return statuses[Math.floor(Math.random() * statuses.length)];
}

